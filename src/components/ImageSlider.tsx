const images = slides.map(s => s.image).concat(slides.map(s => s.image)); // duplicate for smooth scroll

const ImageSlider = () => { 
  const [selectedSlide, setSelectedSlide] = useState<Slide | null>(null);

  const handleClick = (img: string) => {
    const slide = slides.find(s => s.image === img);
    if (slide) setSelectedSlide(slide);
  };

  return (
    <div className="marquee-container">
      <div className="marquee-track"> 
        {images.map((img, i) => (
          <img 
            key={i} 
            src={img} 
            alt="memorial" 
            className="marquee-img cursor-pointer" 
            onClick={() => handleClick(img)}
          />
        ))}
      </div>

      {/* Modal */}
      {selectedSlide && (
        <div className="fixed inset-0 bg-black bg-opacity-70 flex items-center justify-center z-50 p-4">
          <div className="bg-white rounded-lg max-w-xl w-full p-6 relative max-h-[90vh] overflow-y-auto shadow-xl">
            {/* Close Button */}
          <button
                 className="absolute top-3 right-3 text-4xl font-bold hover:opacity-80"
                 style={{ color: "#E01075" }}
                 onClick={() => setSelectedSlide(null)}
                 >
              &times;
            </button>

            {/* Image */}
            <img
              src={selectedSlide.image}
              alt={selectedSlide.title}
              className="w-full h-auto object-cover rounded mb-4"
            />

            {/* Title */}
            <h2 className="text-3xl font-bold mb-4 text-black">{selectedSlide.title}</h2>

            {/* Story */}
            <p className="text-black text-base leading-relaxed">{selectedSlide.story}</p>
          </div>
        </div>
      )}
    </div>
  );
}; 

export default ImageSlider;
